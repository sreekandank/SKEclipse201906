public class Student {
   int rno;
   String sname, dept;

   public int getRno() {
       return rno;
   }

   public void setRno(int rno) {
       this.rno = rno;
   }

   public String getSname() {
       return sname;
   }

   public void setSname(String sname) {
       this.sname = sname;
   }

   public String getDept() {
       return dept;
   }

   public void setDept(String dept) {
       this.dept = dept;
   }
   
}
